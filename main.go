package main

import (
	"fmt"
	"os/exec"
)

func main() {
	cmd := exec.Command(
		"/app/eunod",
		fmt.Sprintf("-conf=%s", "/app/euno-mainnet.conf"),
	)

	stdout, err := cmd.Output()

	if err != nil {
		fmt.Println(err.Error())
		return
	}

	// Print the output
	fmt.Println(string(stdout))
}
